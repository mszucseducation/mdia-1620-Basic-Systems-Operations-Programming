// Comparison Operator - Greater Than

/** Example 1
 * 
 * Declare, initialise and assign a variable x to the value 15. 
 * Declare, initialise and assign a variable y to the value 25. 
 * Then console log out each of the variables.
 * 
 */


 var x = 15;
 var y = 25;
 
 console.log(x > y); 
 console.log(y > x); 
 

/** Example 2
 * 
 * Declare, initialise and assign a variable x to the value 3. 
 * Declare, initialise and assign a variable y to the value 5. 
 * Then console log out each of the variables.
 * 
 */


 var x = 3;
 var y = 5;
 
 console.log(x > y); // false
 console.log(y > x); // true



 