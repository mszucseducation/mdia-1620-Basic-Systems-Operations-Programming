// Comparison Operator - Less Than

/** Example 1
 * 
 * Declare, initialise and assign a variable x to the value 15. 
 * Declare, initialise and assign a variable y to the value 25. 
 * First console log out x less than or equal to y. 
 * Second console log out y less than or equal to x.
 * 
 */


 var x = 15;
 var y = 25;
 
 console.log(x < y); 
 console.log(y < x); 
 

