// Comparison Operator - Greater than or equal to

/** Example 1
 * 
 * Declare, initialise and assign a variable x to the value 12. 
 * Declare, initialise and assign a variable y to the value 13. 
 * First console log out x greater than or equal to y. 
 * Second console log out y greater than or equal to x.
 * 
 */

 var x = 12;
 var y = 13;
 
 console.log(x >= y); 
 console.log(y >= x); 
 
