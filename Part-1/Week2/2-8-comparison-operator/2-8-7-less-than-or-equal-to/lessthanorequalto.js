// Comparison Operator - Less than or equal to

/** Example 1
 * 
 * Declare, initialise and assign a variable x to the value 6. 
 * Declare, initialise and assign a variable y to the value 14. 
 * First console log out x less than or equal to y. 
 * Second console log out y less than or equal to x.
 * 
 */

 var x = 6;
 var y = 14;
 
 console.log(x <= y); 
 console.log(y <= x); 
 


