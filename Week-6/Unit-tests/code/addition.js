/**
 * Calculate the addition of three numbers
 * 
 * @param {*} bagOne equals to 10
 * @param {*} bagTwo equals to 20
 * @param {*} bagThree equals to 30
 * @return the addition of three numbers
 */
function additionFunction(bagOne, bagTwo, bagThree) {
    return bagOne + bagTwo + bagThree;
}

module.exports = {
    additionFunction
};